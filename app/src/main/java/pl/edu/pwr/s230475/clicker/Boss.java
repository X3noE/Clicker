package pl.edu.pwr.s230475.clicker;

public class Boss {
    int Level;
    int HP;
    int Nagroda;

    public int getLevel() {
        return Level;
    }

    public void setLevel(int level) {
        Level = level;
    }

    public int getHP() {
        return HP;
    }

    public void setHP(int HP) {
        this.HP = HP;
    }

    public int getNagroda() {
        return Nagroda;
    }

    public void setNagroda(int nagroda) {
        Nagroda = nagroda;
    }


}
